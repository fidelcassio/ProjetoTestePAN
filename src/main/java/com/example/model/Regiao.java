package com.example.model;

public class Regiao {
	private int id;
	private String sigla;
	private String nome;

	// Getter Methods

	public int getId() {
		return id;
	}

	public String getSigla() {
		return sigla;
	}

	public String getNome() {
		return nome;
	}

	// Setter Methods

	public void setId(int id) {
		this.id = id;
	}

	public void setSigla(String sigla) {
		this.sigla = sigla;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
}
